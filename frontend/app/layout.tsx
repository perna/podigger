import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Podigger - Podcast Discovery",
  description: "Discover and explore podcasts with Podigger",
};

/**
 * Root layout component that provides the HTML document shell and applies app fonts.
 *
 * @param children - The React nodes to render inside the document body.
 * @returns An HTML element containing a <body> with the app font CSS variables and `children` rendered inside.
 */
export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en">
      <body
        className={`${geistSans.variable} ${geistMono.variable} antialiased`}
      >
        {children}
      </body>
    </html>
  );
}